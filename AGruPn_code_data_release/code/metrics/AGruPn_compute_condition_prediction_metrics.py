import json
from pathlib import Path

import numpy as np
import pandas as pd


ROOT = Path(__file__).resolve().parents[2]
RESULT_DIR = ROOT / "data" / "prediction_results"
OUTPUT = ROOT / "outputs" / "tables" / "condition_prediction_metrics.json"

DATASET_MAP = {
    "penglai1": "Site1",
    "penglai2": "Site2",
    "penglai3": "Site3",
    "Aventa": "Aventa",
    "T1spring": "T1 spring",
    "T1summer": "T1 summer",
    "T1autumn": "T1 autumn",
    "T1winter": "T1 winter",
}

CONDITION_MAP = {
    "0": "Normal",
    "1": "Gust",
    "all": "All",
}


def smape(y_true, y_pred):
    return 100 * np.mean(np.abs(y_pred - y_true) / (np.abs(y_true) + np.abs(y_pred) + 1e-8))


def icr(y_true, lower, upper):
    return 100 * np.mean((y_true >= lower) & (y_true <= upper))


def interval_score(y_true, lower, upper, alpha=0.05):
    interval_width = upper - lower
    lower_penalty = (2 / alpha) * (lower - y_true) * (y_true < lower)
    upper_penalty = (2 / alpha) * (y_true - upper) * (y_true > upper)
    return np.mean(interval_width + lower_penalty + upper_penalty)


def metrics_for_file(path):
    df = pd.read_csv(path)
    y_true = df["true_value"].to_numpy(dtype=float)
    y_pred = df["pred_med"].to_numpy(dtype=float)
    lower = df["pred_low"].to_numpy(dtype=float)
    upper = df["pred_high"].to_numpy(dtype=float)
    abs_err = np.abs(y_true - y_pred)
    return {
        "samples": int(len(df)),
        "SMAPE": float(smape(y_true, y_pred)),
        "ICR": float(icr(y_true, lower, upper)),
        "IS": float(interval_score(y_true, lower, upper)),
        "MAE": float(np.mean(abs_err)),
        "RMSE": float(np.sqrt(np.mean((y_true - y_pred) ** 2))),
        "Extreme error rate": float(100 * np.mean(abs_err > np.quantile(abs_err, 0.95))),
        "true_mean": float(np.mean(y_true)),
        "true_std": float(np.std(y_true, ddof=1)),
        "true_max": float(np.max(y_true)),
        "source_file": path.name,
    }


def parse_result_name(path):
    stem = path.stem
    prefix = "quantile_prediction_results_"
    if not stem.startswith(prefix):
        return None
    tail = stem[len(prefix):]
    if "_9_" not in tail:
        return None
    dataset_key, condition_key = tail.rsplit("_9_", 1)
    if dataset_key not in DATASET_MAP or condition_key not in CONDITION_MAP:
        return None
    return DATASET_MAP[dataset_key], CONDITION_MAP[condition_key], condition_key


def main():
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    rows = []
    for path in sorted(RESULT_DIR.glob("quantile_prediction_results_*_9_*.csv")):
        parsed = parse_result_name(path)
        if parsed is None:
            continue
        dataset, condition, condition_key = parsed
        values = metrics_for_file(path)
        rows.append({
            "Dataset": dataset,
            "Condition": condition,
            "Condition key": condition_key,
            "Model": "AGruPn(Ours)",
            **values,
        })

    rows.sort(key=lambda r: (r["Dataset"], {"All": 0, "Normal": 1, "Gust": 2}.get(r["Condition"], 9)))
    payload = {
        "condition_mapping": {
            "0": "Normal / non-triggered regime",
            "1": "Gust / Bollinger-triggered high-risk regime",
            "evidence": [
                "index_flag is generated by df.index.isin(index_list).astype(int), where index_list comes from Bollinger outliers outside the upper/lower bands.",
                "Subsequent code states label_consensus prevalence with 1 = gusty / high-risk regime.",
                "The model0/model1 notebooks split test_loader_0 and test_loader_1 by index_flag == 0 and index_flag == 1, respectively.",
            ],
        },
        "metric_definitions": {
            "SMAPE": "100 * mean(abs(pred_med - true_value) / (abs(true_value) + abs(pred_med) + 1e-8))",
            "ICR": "100 * mean(true_value >= pred_low and true_value <= pred_high)",
            "IS": "mean((pred_high - pred_low) + 2/alpha*(pred_low - true_value)*I(true_value < pred_low) + 2/alpha*(true_value - pred_high)*I(true_value > pred_high)), alpha=0.05",
            "MAE": "mean(abs(true_value - pred_med))",
            "RMSE": "sqrt(mean((true_value - pred_med)^2))",
            "Extreme error rate": "percentage of samples with absolute error above the file-specific 95th percentile",
        },
        "rows": rows,
    }
    OUTPUT.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(rows)} metric rows to {OUTPUT}")
    for row in rows:
        if row["Dataset"] in {"Site1", "Site2", "Site3", "Aventa"} and row["Condition"] in {"Normal", "Gust"}:
            print(
                row["Dataset"],
                row["Condition"],
                "n=", row["samples"],
                "SMAPE=", round(row["SMAPE"], 3),
                "ICR=", round(row["ICR"], 3),
                "IS=", round(row["IS"], 3),
            )


if __name__ == "__main__":
    main()
